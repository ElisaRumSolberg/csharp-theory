
using System;

class Program
{
    static void Main(string[] args)
    {
        // Integer type
        int age = 30;

        // Double type
        double height = 1.75;

        // Char type
        char grade = 'A';

        // String type
        string name = "Elisa";

        // Boolean type
        bool isStudent = true;

        // Print all values
        Console.WriteLine("Name: " + name);
        Console.WriteLine("Age: " + age);
        Console.WriteLine("Height: " + height);
        Console.WriteLine("Grade: " + grade);
        Console.WriteLine("Is Student: " + isStudent);
    }
}
